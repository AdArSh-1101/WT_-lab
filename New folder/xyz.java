public class xyz
{
    private final double a;
    private final double b;
    private final double c;
    private final double d;
    private final String name;
    xyz(double p,double q,double r,double s,String n)
    {
        a=p;
        b=q;
        c=r;
        d=s;
        name=n;
    }
    public double a_()
    {
        return a;
    }
    public double b_()
    {
        return b;
    }
    public double c_()
    {
        return c;
    }
    public double d_()
    {
        return d;
    }
    public String n_()
    {
        return name;
    }

}